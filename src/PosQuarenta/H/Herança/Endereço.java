package PosQuarenta.H.Herança;

public class Endereço {
    private String rua;
    private String bairro;
    
    public void setRua(String rua){
        this.rua = rua;
    }
    public void setBairro(String bairro){
        this.bairro = bairro;
    }
    public String getRua(){
        return this.rua;
    }
    public String getBairro(){
        return this.bairro;
    }
}
